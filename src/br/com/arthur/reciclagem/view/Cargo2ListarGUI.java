    private void btRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRemoverActionPerformed
int linhaSelecionada = -1;
        linhaSelecionada = tabela.getSelectedRow();
        if (linhaSelecionada >= 0) {
            int codigoCargo2 = (int) tabela.getValueAt(linhaSelecionada, 0);
            Cargo2Controller cc = new Cargo2Controller();
            if (cc.remover(codigoCargo2)) {
                JOptionPane.showMessageDialog(null, "Cargo removido com sucesso");
                modelo.removeRow(linhaSelecionada);
            } else {
                JOptionPane.showMessageDialog(null, "Selecione uma linha");
            }
        }
    }//GEN-LAST:event_btRemoverActionPerformed

    private void btEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditarActionPerformed
        int linhaSelecionada = -1;
        linhaSelecionada = tabela.getSelectedRow();
        if (linhaSelecionada >= 0) {
            int codigoCargo2 = (int) tabela.getValueAt(linhaSelecionada, 0);
            Cargo2InserirGUI cc = new Cargo2InserirGUI(modelo, linhaSelecionada, codigoCargo2);
             cc.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "Selecione uma linha");
            }
    }//GEN-LAST:event_btEditarActionPerformed

