    private void desenharTabela() {
        tabela = new JTable(modelo);
        modelo.addColumn("Código");
        modelo.addColumn("Nome");
        modelo.addColumn("Carga Horária");
        preencherTabela();
    }

    private void preencherTabela() {
         Cargo2Controller cc = new Cargo2Controller();
        for (Cargo2 c : cc.listarTodos()) {
            modelo.addRow(new Object[]{c.getCodigo(), c.getCargo2(), c.getHoras()});
        }
    }
